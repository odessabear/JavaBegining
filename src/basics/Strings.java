package basics;

public class Strings {
    public static void main(String[] args) {
        int x = 5;
        String string = "Hello";
        String string1 = " ";
        String string2 = "Bob";
        System.out.println(string + string1 + string2);
        System.out.println("Hello" + " " + "Bob");
        System.out.println("My number is " + x);
    }
}
